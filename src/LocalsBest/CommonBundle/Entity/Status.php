namespace LocalsBest\CommonBundle\Entity;

use Doctrine\ORM\Mapping as ORM;


/**
 * LocalsBest\UserBundle\Entity\Status
 *
 * @ORM\Table(name="statuses")
 * @ORM\Entity(repositoryClass="LocalsBest\CommonBundle\Entity\StatusRepository")
 */
class Status
{
    
    /**
     * @ORM\Column(type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    protected $id;

    /**
     * @ORM\Column(type="string", length=100, nullable=true)
     */
    protected $status;
    
    /**
     * @ORM\Column(type="string", length=100, nullable=true)
     */
    protected $color;
    

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set status
     *
     * @param string $status
     * @return Status
     */
    public function setStatus($status)
    {
        $this->status = $status;

        return $this;
    }

    /**
     * Get status
     *
     * @return string 
     */
    public function getStatus()
    {
        return $this->status;
    }

    /**
     * Set color
     *
     * @param string $color
     * @return Status
     */
    public function setColor($color)
    {
        $this->color = $color;

        return $this;
    }

    /**
     * Get color
     *
     * @return string 
     */
    public function getColor()
    {
        return $this->color;
    }
}
