namespace LocalsBest\UserBundle\Entity;

use Doctrine\ORM\Mapping as ORM;


/**
 * LocalsBest\UserBundle\Entity\BusinessCategory
 *
 * @ORM\Table(name="businessCategory")
 * @ORM\Entity(repositoryClass="LocalsBest\UserBundle\Entity\BusinessCategoryRepository")
 */
class BusinessCategory
{
    
    /**
     * @ORM\Column(type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    protected $id;

    /**
     * @ORM\Column(type="string", length=255, nullable=true)
     */
    protected $name;
    
    

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    

    /**
     * Set name
     *
     * @param string $name
     * @return BusinessCategory
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }
}
